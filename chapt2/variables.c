#include <stdio.h>

int main(void)
{

  float f1 = 7.224, f2, f3;
  int i1, i2 = 4, i3;

  i1 = 32;

  printf("f1 is %f\n", f1);
  printf("f2 is %f\n", f2);
  printf("f3 is %f\n", f3);

  printf("i1 is %i\n", i1);
  printf("i2 is %i\n", i2);
  printf("i3 is %i\n", i3);

  return 0;
}
